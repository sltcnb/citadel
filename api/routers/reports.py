def _md_to_html_minimal(md: str) -> str:
    """Very small Markdown → HTML — enough for our report shape (headings,
    lists, blockquotes, inline code, bold). No external dep."""
    lines = md.splitlines()
    out: list[str] = []
    in_list = False
    for raw in lines:
        line = raw.rstrip()
        if not line:
            if in_list:
                out.append("</ul>")
                in_list = False
            out.append("")
            continue
        if line.startswith("# "):
            if in_list:
                out.append("</ul>")
                in_list = False
            out.append(f"<h1>{_inline(line[2:])}</h1>")
            continue
        if line.startswith("## "):
            if in_list:
                out.append("</ul>")
                in_list = False
            out.append(f"<h2>{_inline(line[3:])}</h2>")
            continue
        if line.startswith("### "):
            if in_list:
                out.append("</ul>")
                in_list = False
            out.append(f"<h3>{_inline(line[4:])}</h3>")
            continue
        if line == "---":
            if in_list:
                out.append("</ul>")
                in_list = False
            out.append("<hr/>")
            continue
        if line.startswith("- ") or line.startswith("  > "):
            if line.startswith("  > "):
                out.append(f"<blockquote>{_inline(line[4:])}</blockquote>")
                continue
            if not in_list:
                out.append("<ul>")
                in_list = True
            out.append(f"<li>{_inline(line[2:])}</li>")
            continue
        if in_list:
            out.append("</ul>")
            in_list = False
        out.append(f"<p>{_inline(line)}</p>")
    if in_list:
        out.append("</ul>")
    return "\n".join(out)


def _inline(s: str) -> str:
    import re as _re

    # escape, then re-apply allowed inline marks
    s = s.replace("&", "&amp;").replace("<", "&lt;").replace(">", "&gt;")
    s = _re.sub(r"`([^`]+)`", r"<code>\1</code>", s)
    s = _re.sub(r"\*\*([^*]+)\*\*", r"<strong>\1</strong>", s)
    s = _re.sub(r"_([^_]+)_", r"<em>\1</em>", s)
    return s


# ── Report template admin endpoints ──────────────────────────────────────────

from auth.dependencies import require_admin  # noqa: E402
from pydantic import BaseModel  # noqa: E402


class ReportTemplateIn(BaseModel):
    title_prefix: str = "Investigation report"
    header_md: str = ""
    footer_md: str = "_Generated by Citadel_"
    max_flagged: int = 50
    sections: dict = {}


@router.get("/admin/report-template", dependencies=[Depends(require_admin)])
def get_report_template():
    return _load_template()


@router.put("/admin/report-template", dependencies=[Depends(require_admin)])
def set_report_template(body: ReportTemplateIn):
    sections = dict(_TEMPLATE_DEFAULTS["sections"])
    for k, v in (body.sections or {}).items():
        if k in sections:
            sections[k] = bool(v)
    tpl = {
        "title_prefix": (body.title_prefix or "Investigation report")[:120],
        "header_md": (body.header_md or "")[:4000],
        "footer_md": (body.footer_md or "")[:1000],
        "max_flagged": min(max(1, body.max_flagged), 500),
        "sections": sections,
    }
    get_redis().set(_REPORT_TEMPLATE_KEY, json.dumps(tpl))
    return tpl


@router.delete("/admin/report-template", dependencies=[Depends(require_admin)])
def reset_report_template():
    get_redis().delete(_REPORT_TEMPLATE_KEY)
    return _load_template()
