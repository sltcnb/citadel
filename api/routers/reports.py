"""
Investigation report generator.

Pulls everything needed to deliver a case writeup:
  - Case metadata
  - Pinned events (curated evidence) — top of report
  - Flagged events (triage queue) — mid report
  - Analyst notes (Markdown WYSIWYG)
  - MITRE technique coverage
  - Watchlist hits (auto-eval)
  - Detection rule matches

Renders Markdown by default. PDF generation is optional (requires WeasyPrint
or similar — falls back to plain text if unavailable).
"""

