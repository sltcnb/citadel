def _fetch_mitre(case_id: str) -> dict:
    # Field shapes mirror search.py mitre_coverage: live data uses mitre.id
    # (text → aggregate on .keyword) and mitre.technique (.keyword), with
    # mitre.tactic mapped as keyword directly.
    body = {
        "size": 0,
        "query": {"exists": {"field": "mitre.id"}},
        "aggs": {
            "by_technique": {
                "terms": {"field": "mitre.id.keyword", "size": 100},
                "aggs": {
                    "top_name": {"terms": {"field": "mitre.technique.keyword", "size": 1}},
                    "top_tactic": {"terms": {"field": "mitre.tactic", "size": 1}},
                },
            },
        },
    }
    try:
        res = es_req("POST", f"/fo-case-{case_id}-*/_search", body)
    except Exception:
        return {"techniques": []}
    techniques = []
    for b in res.get("aggregations", {}).get("by_technique", {}).get("buckets", []):
        names = b.get("top_name", {}).get("buckets", [])
        tactics = b.get("top_tactic", {}).get("buckets", [])
        techniques.append(
            {
                "id": b["key"],
                "name": names[0]["key"] if names else b["key"],
                "tactic": tactics[0]["key"] if tactics else "",
                "count": b["doc_count"],
            }
        )
    return {"techniques": techniques}


def _fetch_watchlist_run(case_id: str) -> dict:
    raw = get_redis().get(f"fo:watchlist_runs:{case_id}")
    if not raw:
        return {}
    try:
        return json.loads(raw.decode() if isinstance(raw, bytes) else raw)
    except Exception:
        return {}


def _fetch_detection_run(case_id: str) -> dict:
    raw = get_redis().get(rk.case_alert_run(case_id))
    if not raw:
        return {}
    try:
        return json.loads(raw.decode() if isinstance(raw, bytes) else raw)
    except Exception:
        return {}


def _fetch_notes(case_id: str) -> str:
    raw = get_redis().get(f"case:{case_id}:notes")
    if not raw:
        return ""
    s = raw.decode() if isinstance(raw, bytes) else raw
    # Strip surrounding JSON if stored that way
    try:
        return json.loads(s).get("body", s) if s.startswith("{") else s
    except Exception:
        return s


def _fetch_ai_report(case_id: str) -> dict | None:
    """Pull the AI Investigation Report (auto-written by the Autopilot agent
    or generated manually). Format: { content, generated_at, model_used, ... }"""
    raw = get_redis().get(f"case:{case_id}:ai:report")
    if not raw:
        return None
    s = raw.decode() if isinstance(raw, bytes) else raw
    try:
        return json.loads(s)
    except Exception:
        return None


def _event_line(ev: dict) -> str:
    """Single-line event summary for the report."""
    ts = _ts(ev.get("timestamp", ""))
    atype = ev.get("artifact_type", "?")
    host = (ev.get("host") or {}).get("hostname", "")
    user = (ev.get("user") or {}).get("name", "")
    msg = (ev.get("message") or "").strip().splitlines()[0][:240]
    bits = [f"`{ts}`" if ts else "", f"**{atype}**"]
    if host:
        bits.append(f"@ `{host}`")
    if user:
        bits.append(f"user `{user}`")
    bits.append(f"— {msg}" if msg else "")
    return " ".join(b for b in bits if b)


def _render_markdown(
    case: dict,
    pinned: list,
    flagged: list,
    mitre: dict,
    wl: dict,
    det: dict,
    notes: str,
    ai_report: dict | None = None,
    tpl: dict | None = None,
) -> str:
    tpl = tpl or _load_template()
    sections = tpl.get("sections") or {}
    out: list[str] = []
    name = case.get("name", case.get("case_id", "Case"))
    out.append(f"# {tpl.get('title_prefix') or 'Investigation report'} — {name}")
    out.append("")
    if (tpl.get("header_md") or "").strip():
        out.append(tpl["header_md"].strip())
        out.append("")
    out.append(f"_Generated {_ts(datetime.now(UTC).isoformat())}_")
    if case.get("company"):
        out.append(f"_Company: {case['company']}_")
    out.append("")

    # Executive summary derived from the data we have
    if sections.get("exec_summary", True):
        out.append("## Executive summary")
        out.append("")
        out.append(f"- **{len(pinned)} pinned event(s)** curated as key evidence.")
        out.append(f"- **{len(flagged)} flagged event(s)** under analyst review.")
        out.append(f"- **{len(mitre.get('techniques', []))} MITRE ATT&CK techniques** evidenced.")
        if wl and wl.get("hits"):
            out.append(f"- **{len(wl['hits'])} IOC watchlist hit(s)** in the latest sweep.")
        if det and det.get("matches"):
            out.append(f"- **{len(det['matches'])} detection rule(s)** fired.")
        out.append("")

    # ── AI Autopilot investigation (when one ran on this case) ──────────────
    # Folded into the flat report so the non-AI deliverable still surfaces
    # the agent's findings. The block is the LLM-polished markdown already,
    # so we just inline it under its own H2.
    if sections.get("ai_report", True) and ai_report and (ai_report.get("content") or "").strip():
        out.append("---")
        out.append("")
        out.append("## AI Autopilot investigation")
        out.append("")
        meta_bits = []
        if ai_report.get("generated_at"):
            meta_bits.append(f"_generated {_ts(ai_report['generated_at'])}_")
        if ai_report.get("model_used"):
            meta_bits.append(f"_{ai_report['model_used']}_")
        if ai_report.get("source"):
            meta_bits.append(f"_source: {ai_report['source']}_")
        if meta_bits:
            out.append(" · ".join(meta_bits))
            out.append("")
        # Strip any top-level "# " heading from the AI content so the section
        # nesting stays tidy (the LLM emits "# Executive Summary" which would
        # otherwise outrank our case title).
        ai_content = (ai_report["content"] or "").strip()
        ai_content = "\n".join(
            ("##" + ln[1:]) if ln.startswith("# ") else ln for ln in ai_content.splitlines()
        )
        out.append(ai_content)
        out.append("")

    # Pinned events (the curated narrative)
    if sections.get("pinned", True) and pinned:
        out.append("## Key evidence (pinned events)")
        out.append("")
        for ev in pinned:
            out.append(f"- {_event_line(ev)}")
            if ev.get("pin_note"):
                out.append(f"  > _{ev['pin_note']}_")
        out.append("")

    # Flagged
    max_flagged = int(tpl.get("max_flagged") or 50)
    if sections.get("flagged", True) and flagged:
        out.append("## Flagged events")
        out.append("")
        for ev in flagged[:max_flagged]:
            out.append(f"- {_event_line(ev)}")
        if len(flagged) > max_flagged:
            out.append(f"_…and {len(flagged) - max_flagged} more._")
        out.append("")

    # MITRE
    techs = mitre.get("techniques", []) if sections.get("mitre", True) else []
    if techs:
        out.append("## MITRE ATT&CK coverage")
        out.append("")
        by_tactic: dict[str, list] = {}
        for t in techs:
            by_tactic.setdefault(t["tactic"] or "Unknown", []).append(t)
        for tactic, items in sorted(by_tactic.items()):
            out.append(f"### {tactic}")
            for t in sorted(items, key=lambda x: -x["count"]):
                out.append(f"- `{t['id']}` {t['name']} — {t['count']} event(s)")
            out.append("")

    # Detection rules
    if sections.get("detections", True) and det and det.get("matches"):
        out.append("## Detection rules that fired")
        out.append("")
        out.append(f"_Last run: {_ts(det.get('ran_at', ''))}_")
        for m in det["matches"]:
            rule = m.get("rule") or {}
            out.append(f"- **{rule.get('name', '?')}** ({m.get('match_count', 0)} match(es))")
            if rule.get("description"):
                out.append(f"  > {rule['description']}")
        out.append("")

    # Watchlist
    if sections.get("watchlist", True) and wl and wl.get("hits"):
        out.append("## IOC watchlist hits")
        out.append("")
        out.append(f"_Auto-sweep: {_ts(wl.get('ran_at', ''))}_")
        for h in wl["hits"]:
            out.append(f"- **{h.get('label')}** (`{h.get('kind')}`) — {h.get('hits')} match(es)")
            out.append(f"  - Query: `{h.get('query')}`")
        out.append("")

    # Notes
    if sections.get("notes", True) and notes:
        out.append("## Analyst notes")
        out.append("")
        out.append(notes)
        out.append("")

    out.append("---")
    if (tpl.get("footer_md") or "").strip():
        out.append(tpl["footer_md"].strip())
    return "\n".join(out)


@router.get("/cases/{case_id}/report.md")
def report_markdown(case_id: str, case: dict = Depends(require_case_access)):
    md = _render_markdown(
        case=case,
        pinned=_fetch_pinned(case_id),
        flagged=_fetch_flagged(case_id),
        mitre=_fetch_mitre(case_id),
        wl=_fetch_watchlist_run(case_id),
        det=_fetch_detection_run(case_id),
        notes=_fetch_notes(case_id),
        ai_report=_fetch_ai_report(case_id),
    )
    safe_name = (case.get("name") or case_id).replace("/", "_")[:80]
    return Response(
        content=md,
        media_type="text/markdown; charset=utf-8",
        headers={"Content-Disposition": f'attachment; filename="{safe_name}-report.md"'},
    )


